package com.exp.model.pojo;

public class TLog {
    private Integer id;
    private Integer mid;
    private String date;
    private Double t1;
    private Double t2;
    private String t1t;
    private String t2t;
    private String notes;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getMid() {
        return mid;
    }

    public void setMid(Integer mid) {
        this.mid = mid;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public Double getT1() {
        return t1;
    }

    public void setT1(Double t1) {
        this.t1 = t1;
    }

    public Double getT2() {
        return t2;
    }

    public void setT2(Double t2) {
        this.t2 = t2;
    }

    public String getT1t() {
        return t1t;
    }

    public void setT1t(String t1t) {
        this.t1t = t1t;
    }

    public String getT2t() {
        return t2t;
    }

    public void setT2t(String t2t) {
        this.t2t = t2t;
    }

    public String getNotes() {
        return notes;
    }

    public void setNotes(String notes) {
        this.notes = notes;
    }
}
